<h2>Eligibility:</h2>


<ul><li>Adult 12yrs +</li>
<li>Child (6-11 years): Age at the time of exchanging order is issued. </li>
<li>Child 5 and under is free of charge with NO seat.<br /></li>

<li><strong>Non Japanese Nationals: </strong>  Must be visiting Japan under the entry status of "Temporary visitor" according to Japan Immigration Law. When entering Japan for sightseeing, entry personnel will stamp the passport as "Temporary visitor" Only a person who has a passport bearing this stamp can use a JAPAN RAIL PASS. Please note that according to strict interpretation of the Japanese Immigration Law, “Temporary visitor" status differs even from other types of stays that are also for only short time periods. <br /></</li>
<li><strong>Japanese Nationals: </strong>  Who have both their valid Japanese passport and written proof obtained from the embassy or legation of Japan in the foreign country where they live, that they have been living in that country for 10 or more consecutive years. <br /></</li>
<li>It is Japan Rail’s strict rule that the JR passes have to be sold at the price printed on the pass and that no discounting is allowed under any circumstances. Japan Rail is closely monitoring the selling prices at all times and if any proof of discounting is found, the given agents will not be able to sell the passes any more. <br /></</li>
</ul>


